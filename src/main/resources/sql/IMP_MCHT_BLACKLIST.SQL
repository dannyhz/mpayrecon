--跑批库版本
CREATE TABLE MPOS.IMP_MCHT_BLACKLIST (
  ID	DECIMAL(20,0)	,
  CRT_TM	TIMESTAMP ,
  MDF_TM	TIMESTAMP ,
  MCH_NO	VARCHAR(50)	DEFAULT ,
  SEC_MCH_NO	VARCHAR(50)	DEFAULT ,
  CHANNEL_NO	VARCHAR(50)	DEFAULT ,
  BLACK_TP	CHARACTER(1)	DEFAULT ,
  RECORD_ST	SMALLINT	DEFAULT 1,
  DESC	VARCHAR(200)	DEFAULT 
);

COMMENT ON TABLE MPOS.IMP_MCHT_BLACKLIST IS '商户黑名单';

COMMENT ON MPOS.IMP_MCHT_BLACKLIST ( 
	ID IS 'ID',
	CRT_TM IS '入库时间戳',
	MDF_TM IS '修改时间戳',
	MCH_NO IS '商户号,普通商户|独立子商户|平台商户',
	SEC_MCH_NO IS '二级商户号，暂时不用',
	CHANNEL_NO IS '渠道',
	BLACK_TP IS '黑名单类型, C=结算黑名单；T=交易黑名单；L=信用卡限额',
	RECORD_ST IS '记录状态；1=生效；0=失效',
	DESC IS '记录描述,用于表述生效或失效原因' );

