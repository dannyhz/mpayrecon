--通过NK_BAT2_FLAG进行访问
--主库版本
CREATE TABLE MPOS.BAT2_FLAG (
	ID	DEC(20,0)	NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1,INCREMENT BY 1,NO CACHE),
	SYSTM	TIMESTAMP	with default CURRENT_TIMESTAMP,
	KEY VARCHAR(500),
	VAL1	VARCHAR(100),
	VAL2	VARCHAR(200),
	VAL3	VARCHAR(500),
 	primary key (ID)	
); 

CREATE UNIQUE INDEX MPOS.BAT2_FLAG_IDX1 ON MPOS.BAT2_FLAG(KEY);

COMMENT ON TABLE MPOS.BAT2_FLAG IS '跑批阶段标记表';

COMMENT ON MPOS.BAT2_FLAG ( 
	ID IS 'ID',
	SYSTM is '入库时间戳',
	KEY is 'KEY',
	VAL1 is 'VALUE1',
	VAL2 is 'VALUE2',
	VAL3 is 'VALUE3'
);


-----
CREATE OR REPLACE NICKNAME MPOS.NK_BAT2_FLAG
  FOR SVRWR.MPOS.BAT2_FLAG;
  