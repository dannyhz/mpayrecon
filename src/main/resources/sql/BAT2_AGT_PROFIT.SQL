CREATE TABLE MPOS.BAT2_AGT_PROFIT (
	ID	DEC(20,0)	NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1,INCREMENT BY 1,NO CACHE),
	SYSTM	TIMESTAMP	with default CURRENT_TIMESTAMP,
	BATID DEC(20),
	CALDT	CHAR(8),
	MCH_NO	VARCHAR(50) with default '',
	MY_SEC_MCH_NO VARCHAR(50) with default '',
  AGNT_BRH_ID	VARCHAR(10)	NOT NULL,
  AGNT_BRH_NAME	VARCHAR(100),
  PROFIT_RATE	VARCHAR(48)	NOT NULL,
	PROFIT_FEE DEC(15,2),
	CNT INT,
	TRAM DEC(15,2),
	COST	DEC(15,2) ,
	TOTAL_FEE	DEC(15,2),
	MY_PTMCH_FEE DEC(15,2),
 	primary key (ID)	
); 

CREATE INDEX MPOS.BAT2_AGT_PROFIT_IDX1 ON MPOS.BAT2_AGT_PROFIT(CALDT,AGNT_BRH_ID);
CREATE INDEX MPOS.BAT2_AGT_PROFIT_IDX2 ON MPOS.BAT2_AGT_PROFIT(BATID);

COMMENT ON TABLE MPOS.BAT2_AGT_PROFIT IS '分润明细表';

COMMENT ON MPOS.BAT2_AGT_PROFIT ( 
	ID IS 'ID',
	SYSTM is '入库时间戳',
	BATID is '对账批次号',
	CALDT is '计算日期',
	MCH_NO is '渠道方商户号',
	MY_SEC_MCH_NO is '我方二级商户号（分账模式下为平台商户号）',
	AGNT_BRH_ID IS '分润机构id',
	AGNT_BRH_NAME IS '分润机构名称',
	PROFIT_RATE IS '分润费率',
	PROFIT_FEE is '分润',
	CNT is '交易笔数',
	TRAM IS '交易金额',
	COST IS '渠道成本',
	TOTAL_FEE is '商户手续费',
	MY_PTMCH_FEE is '平台商户利差金额'
);


