--通过NK_BAT2_DAY_REPORT进行访问
--主库版本
CREATE TABLE MPOS.BAT2_DAY_REPORT (
	ID	bigint	NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1,INCREMENT BY 1,NO CACHE),
	SYSTM	TIMESTAMP	with default CURRENT_TIMESTAMP,
	TRDT INT NOT NULL,
	KEY1 VARCHAR(100) NOT NULL,
	KEY2 VARCHAR(200),
	KEY3 VARCHAR(300),
	VAL1	VARCHAR(100),
	VAL2	VARCHAR(200),
	VAL3	VARCHAR(300),
	VAL4	VARCHAR(100),
	VAL5	VARCHAR(100),
	MEMO	VARCHAR(100),
 	primary key (ID)	
)
;

CREATE INDEX MPOS.IDX_BAT2_DAY_REPORT_1 ON MPOS.BAT2_DAY_REPORT(TRDT,KEY1,KEY2,KEY3,VAL1) ;

COMMENT ON TABLE MPOS.BAT2_DAY_REPORT IS '对账清算日报基础表';

COMMENT ON MPOS.BAT2_DAY_REPORT ( 
	ID IS 'ID',
	SYSTM is '入库时间戳',
	TRDT is '日期',
	KEY1 is 'KEY1',
	KEY2 is 'KEY2',
	KEY3 is 'KEY3',
	VAL1 is 'VALUE1',
	VAL2 is 'VALUE2',
	VAL3 is 'VALUE3',
	VAL4 is 'VALUE4',
	VAL5 is 'VALUE5',
	MEMO IS '备注'
);

--mposbat
CREATE OR REPLACE NICKNAME MPOS.NK_BAT2_DAY_REPORT
  FOR SVRWR.MPOS.BAT2_DAY_REPORT
