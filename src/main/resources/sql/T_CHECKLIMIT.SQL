CREATE TABLE MPOS.T_CHECKLIMIT
(
SYSTM TIMESTAMP DEFAULT CURRENT TIMESTAMP,
YWTAG	VARCHAR(50) NOT NULL,
CHECKSTH VARCHAR(100) NOT NULL,
CNT 	BIGINT,	
PRIMARY KEY(YWTAG,CHECKSTH)
)
;

COMMENT ON TABLE MPOS.T_CHECKLIMIT IS '额度/次数控制表';

COMMENT ON MPOS.T_CHECKLIMIT (
SYSTM IS '系统时间',
YWTAG is '业务标记',
CHECKSTH is '检查主体',
CNT is '额度/次数'
);



